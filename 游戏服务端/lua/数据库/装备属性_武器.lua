    local t
    if 级别 < 11 and not 强制 then
        return
    else
        if not _数值范围[类型] or not _数值范围[类型][属性] then
            return
        end
        if 强制 and 级别 < 11 then
            级别 = 11
        end
        t = _数值范围[类型][属性][级别]
    end
    if t then
        return math.random(t[1] * 10, t[2] * 10) / 10
    end
    return 0
end

return {
    取属性要求 = function(级别, 类型)
        if 级别 < 11 then
            return
        end
        if not _属性范围[类型] then
            return
        end
        local t = _属性范围[类型]['属性'][级别] or _属性范围[类型]['属性'][11]
        if t then
            local 属性 = 随机表(t)
            return { 属性, _属性要求[级别][属性] }
        end
    end,
    取等级需求 = 取等级需求,
    取攻击 = function(级别)
        local t = _基础攻击[级别]
        if t then
            local a, b, c = table.unpack(t)
            return { '基础攻击', math.floor(a * math.random(b * 1000, c * 1000) / 1000) }
        end
        return { '基础攻击', 0 }
    end,
    取攻击2 = function(级别, 属性)
        local t = _基础攻击[级别][属性]
        if t then
            return { '基础攻击', math.floor(t * math.random(0.8 * 1000, 1.2 * 1000) / 1000) }
        end
        return { '基础攻击', 0 }
    end,
    取物理 = function(级别)
        local 名称 = _物理范围[math.random(5)]
        if 名称 then
            local 数值 = 取武器数值(名称, '根骨', 级别, 1)
            return { 名称, 数值 }
        end
    end,
    --武器级别,属性要求，武器类型，附加名称，附加范围
    取强法 = function(级别, 属性, 类型, 名称, 范围)
        if 级别 < 11 then
            return
        end
        if not 名称 then
            local t = _属性范围[类型] and _属性范围[类型][属性]
            if not t or not t[11] then
                print("武器取强法错误", 级别, 属性, 类型, 名称, 范围)
                return { '基础攻击', 0 }
            end
            名称 = 随机表(t[范围 or 级别] or t[11])
        end
        if 名称 then
            local 数值 = 取武器数值(名称, 属性, 级别)
            return { 名称, 数值 }
        end
    end
}
