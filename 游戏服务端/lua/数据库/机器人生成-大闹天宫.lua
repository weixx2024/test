local function _随机种族()
    local r = math.random(100)
    if r < 20 then
        return 1 -- 人
    end
    if r < 50 then
        return 3 -- 仙
    end
    return 2     -- 魔多刷点
end

local function _随机未转角色(种族)
    local 种族 = 种族 or _随机种族()
    local 性别 = math.random(2)
    local 模型 = math.random(3)
    local 外形 = (种族 - 1) * 6 + (性别 - 1) * 3 + 模型
    return 种族, 性别, 外形
end

local function _随机一转角色(种族)
    local 种族 = 种族 or _随机种族()
    local 性别 = math.random(2)
    local 外形 = 40 + (种族 - 1) * 2 + (性别 - 1)
    return 种族, 性别, 外形
end

local function _随机三转角色(种族)
    local 种族 = 种族 or _随机种族()
    local 性别 = math.random(2)
    local 外形 = 50 + (种族 - 1) * 2 + (性别 - 1)
    return 种族, 性别, 外形
end

