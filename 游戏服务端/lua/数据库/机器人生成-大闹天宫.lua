function 生成召唤数据(t)
    -- 根据模板生成召唤
    local 召唤 = GGF.复制表(召唤模板)
    local 等级 = t.等级
    local 转生 = t.转生
    --================================================================

    召唤.nid = __生成ID()
    召唤.等级 = 等级
    召唤.转生 = 转生
    召唤.是否机器人 = true

    if t.名称 then
        local s = _召唤库[t.名称]
        if s then
            召唤.原名 = s.名称
            召唤.外形 = s.外形
            召唤.现名 = s.名称
            -- 数据
            召唤.数据.染色 = s.染色
            召唤.数据.初攻 = s.初攻
            召唤.数据.初敏 = s.初敏
            召唤.数据.初法 = s.初法
            召唤.数据.初血 = s.初血
            召唤.数据.成长 = s.成长

            召唤.数据.根骨 = 等级
            召唤.数据.灵性 = 等级
            召唤.数据.力量 = 等级 * 5
            召唤.数据.敏捷 = 等级
        end
    end

    local 召唤内丹 = {}
    if 转生 >= 2 then
        table.insert(召唤内丹, _生成召唤内丹({ 名称 = '暗渡陈仓', 等级 = 等级, 转生 = 转生 }))
    end
    if 转生 >= 1 then
        table.insert(召唤内丹, _生成召唤内丹({ 名称 = '借力打力', 等级 = 等级, 转生 = 转生 }))
    end
    if 转生 >= 0 then
        table.insert(召唤内丹, _生成召唤内丹({ 名称 = '浩然正气', 等级 = 等级, 转生 = 转生 }))
    end
    召唤.数据.内丹 = 召唤内丹
    return 召唤
end

