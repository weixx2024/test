function 事件:攻击事件(玩家, NPC)
    if not 玩家.是否队长 then
        return "3人或者3人以上挑战"
    end
    if 玩家:取队伍人数() < 3 then
        return "3人或者3人以上挑战"
    end
    if 玩家:取队伍等级是否低于(0, 90) then
        return "队伍内有低于0转90级的玩家无法挑战该任务"
    end
    if not NPC:是否战斗中() then
        NPC:进入战斗(true)
        local r = 玩家:进入战斗('scripts/event/活动7_年年有余_鲤鱼.lua', NPC)
        NPC:进入战斗(false)
        if r then
            NPC:删除()
            self:完成(玩家)
        end
    else
        return "我正在战斗中"
    end
end

function 事件:NPC菜单(玩家, i)

    if i == '1' then
        if not self:是否战斗中() then
            self:进入战斗(true)
            local r = 玩家:进入战斗('scripts/event/活动7_年年有余_鲤鱼.lua', self)
            self:进入战斗(false)
            if r then
                self:删除()
                self:完成(玩家)
            end
        else
            return "我正在战斗中"
        end
    end
end

