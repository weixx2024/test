function 事件:NPC菜单(玩家, i)
    if i == '1' or i == '2' or i == '3' or i == '4' then
        local r = 玩家:进入战斗('scripts/event/活动7_月光爱人_齐力擎天.lua', self, i)
        if r then
            self:完成(玩家, i)
        end
    elseif i == '5' then
        local t = {}
        if not 玩家.是否队长 then
            return "3人或者3人以上挑战"
        end
        if 玩家:取队伍人数() < 3 then
            return "3人或者3人以上挑战"
        end

        for _, v in 玩家:遍历队伍() do
            if v:判断等级是否低于(90) then
                table.insert(t, v.名称)
            end
        end

        if #t > 0 then
            return table.concat(t, '、 ') .. '#W低于90级,无法挑战该任务!'
        end

        for k, v in 玩家:遍历队伍() do
            if v:取活动限制次数("神木护法") < 1 or v:取活动限制次数("离火护法") < 1 or v:取活动限制次数("玄水护法") < 1 or v:取活动限制次数("赤金护法") < 1 then
                table.insert(t, v.名称)
            end
        end

        if #t > 0 then
            return table.concat(t, "、") .. "还没有通过四大护法的考验"
        end

        for k, v in 玩家:遍历队伍() do
            if v:取活动限制次数("齐力擎天") > 0 then
                table.insert(t, v.名称)
            end
        end

        if #t > 0 then
            return table.concat(t, "、") .. "今日已经挑战过该活动了"
        end

        local r = 玩家:进入战斗('scripts/event/活动7_月光爱人_齐力擎天.lua', self, i)
        if r then
            self:完成(玩家, i)
        end
    end
end

