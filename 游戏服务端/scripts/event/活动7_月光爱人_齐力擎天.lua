local 事件 = {
    名称 = '齐力擎天',
    是否打开 = true,
    是否可接任务 = true,
}

function 事件:事件初始化()
    -- 开启时间：周日中午12点到晚上22点
    if os.date('%w', os.time()) == '7' then
        print('星期天，开启齐力擎天活动')
        local year = tonumber(os.date('%Y', os.time()))
        local month = tonumber(os.date('%m', os.time()))
        local day = tonumber(os.date('%d', os.time()))
        self.开始时间 = os.time { year = year, month = month, day = day, hour = 12, min = 00, sec = 00 }
        self.结束时间 = os.time { year = year, month = month, day = day, hour = 22, min = 00, sec = 00 }
        self.是否结束 = false
    end
end

local _主怪信息 = {
    { 名称 = '穷奇', 模型 = 3863, 地图 = 101381, X = 168, Y = 134, 方向 = 1 },
}

function 事件:清除所有怪物()
    for i = 1, #_主怪信息 do
        local map = self:取地图(_主怪信息[i].地图)
        for k, v in map:遍历NPC() do
            if v.名称 == _主怪信息[i].名称 then
                v:删除()
            end
        end
    end
end

function 事件:更新()
    self:清除所有怪物()
    for k, v in pairs(_主怪信息) do
        local map = self:取地图(v.地图)
        local X, Y = map:取随机坐标()
        local NPC = map:添加NPC {
            名称 = v.名称,
            外形 = v.模型,
            称谓 = '力战穷奇',
            方向 = v.方向,
            脚本 = 'scripts/event/活动7_月光爱人_齐力擎天.lua',
            X = v.X,
            Y = v.Y,
            来源 = self
        }
    end
end

function 事件:事件开始()
    self:更新()
    print('齐力擎天活动开始')
end

function 事件:事件结束()
    self:清除所有怪物()
    print('齐力擎天活动结束')
end

