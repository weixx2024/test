local 事件 = {
    名称 = '花灯报吉_天官',
    是否打开 = true,
}

function 事件:事件初始化()
    -- 开启时间：周日早8点到晚10点
    if os.date('%w', os.time()) == '7' then
        print('星期天，开启花灯报吉活动')
        local year = tonumber(os.date('%Y', os.time()))
        local month = tonumber(os.date('%m', os.time()))
        local day = tonumber(os.date('%d', os.time()))
        self.开始时间 = os.time { year = year, month = month, day = day, hour = 08, min = 00, sec = 00 }
        self.结束时间 = os.time { year = year, month = month, day = day, hour = 22, min = 00, sec = 00 }
        self.是否结束 = false
    end
end

local _地图 = { 1193, 1208 }

function 事件:清除所有怪物()
    for ii, vv in ipairs(_地图) do
        local map = self:取地图(vv)
        for k, v in map:遍历NPC() do
            if v.名称 == '赐福天官' then
                v:删除()
            end
        end
    end
end

function 事件:更新()
    if self.是否结束 then
        return
    end
    self.时间 = os.time() + 60 * 60
    self:清除所有怪物()
    local map = self:取地图(_地图[math.random(1, 2)])
    local 方向 = math.random(1, 4)
    local X, Y = map:取随机坐标()
    local NPC = map:添加NPC {
        名称 = '赐福天官',
        称谓 = '花灯报吉',
        外形 = 2126,
        方向 = 方向,
        脚本 = 'scripts/event/活动7_花灯报吉_天官.lua',
        时间 = self.时间,
        X = X,
        Y = Y,
        来源 = self
    }
    print(string.format('赐福天官坐标 地图：%s X：%s Y:%s', map.名称, X, Y))
    self:发送系统('#G玉帝见诸位不辞劳苦，特派天官下凡来#W东海渔村、长安城东#G犒劳各位，只要能找到天官并通过考验就能成功得到丰厚奖励哦#97')
    return 3600
end

function 事件:事件开始()
    self:更新()
    self:定时(3600, self.更新)
    print('花灯报吉_天官活动开始')
end

function 事件:事件结束()
    self:清除所有怪物()
    print('花灯报吉_天官活动结束')
end

