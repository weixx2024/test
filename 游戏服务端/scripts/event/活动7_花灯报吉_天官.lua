function 事件:完成(玩家)
    if 玩家.是否组队 then
        for _, v in 玩家:遍历队伍() do
            self:掉落包(v)
        end
    else
        self:掉落包(玩家)
    end
end

local _广播 = '#R%s#C通过了天官的考验，得到了一个#G#m(%s)[%s]#m#n#112'

function 事件:掉落包(玩家)
    if 玩家:取活动限制次数('花灯报吉_天官') >= 5 then
        return
    end
    玩家:增加活动限制次数('花灯报吉_天官')

    local 经验 = 1069300
    玩家:添加任务经验(经验)
    玩家:添加法宝经验(500)

    local 掉落包 = 取掉落包('活动', '花灯报吉')
    if 掉落包 then
        奖励掉落包物品(玩家, 掉落包['天官'], _广播)
    end
end

return 事件
