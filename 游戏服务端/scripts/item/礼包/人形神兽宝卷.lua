local 物品 = {
    名称 = '人形神兽宝卷',
    叠加 = 999,
    类别 = 10,
    类型 = 0,
    对象 = 1,
    条件 = 2,
    绑定 = false
}


local _人形神兽范围 = {'泾河龙王','龙太子','青霞仙子','白晶晶','至尊小宝','春三十娘','牛魔王','紫霞仙子'}

function 物品:使用(对象)
    local mc = _人形神兽范围[math.random( #_人形神兽范围)]
    if 对象:添加召唤(生成召唤 { 名称 =mc }) then
        self.数量 = self.数量 - 1
    end
end

return 物品

