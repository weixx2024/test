local 物品 = {
    名称 = '老版神兽宝卷',
    叠加 = 999,
    类别 = 10,
    类型 = 0,
    对象 = 1,
    条件 = 2,
    绑定 = false
}


local _老版神兽范围 = {'超级海龟','超级蝙蝠','超级毒蛇','超级蜘蛛','超级蟾蜍','超级飞鱼'}

function 物品:使用(对象)
    local mc = _老版神兽范围[math.random( #_老版神兽范围)]
    if 对象:添加召唤(生成召唤 { 名称 =mc }) then
        self.数量 = self.数量 - 1
    end
end

return 物品

