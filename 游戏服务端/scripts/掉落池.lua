-- ========================================
function 随机物品(t)
    local 总几率 = 0
    for _, v in ipairs(t) do
        总几率 = 总几率 + v.几率
    end

    local 随机数 = math.random(总几率)
    local 开始几率 = 0
    for _, v in ipairs(t) do
        local 结束几率 = 开始几率 + v.几率
        if 随机数 > 开始几率 and 随机数 <= 结束几率 then
            return v
        end
        开始几率 = 结束几率
    end

    return nil
end

-- ========================================
function 奖励掉落包物品(玩家, 掉落包, 广播)
    local v = 随机物品(掉落包)
    if v and v.名称 ~= '' then
        local 物品 = 生成物品(v)

        if 物品 then
            local r = 玩家:添加物品({ 物品 })
            if r and v.广播 and 广播 then
                玩家:发送系统(广播, 玩家.名称, v.nid, v.名称)
            end
        end
    end
end
