function 任务:取对话(玩家)
    local r = _台词[self.对话进度]
    local 台词, 头像, 结束 = r.台词, r.头像, r.结束
    if 头像 == 0 then
        头像 = 玩家.原形
    end
    return 台词, 头像, 结束
end

function 任务:任务NPC对话(玩家, NPC)
    -- self.进度 = 10
    -- self.对话进度 = 89
    if not 玩家:剧情称谓是否存在(10) then
        return
    end

    NPC.队伍对话 = true
    if NPC.名称 == '夕阳武士' then
        if self.进度 == 0 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 13 then
                self.进度 = 1
            end
        elseif self.进度 == 10 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 90 then
                玩家:删除物品('附魂珠', 1)
                self:完成(玩家)
            end
        end
    elseif NPC.名称 == '菩提祖师' then
        if self.进度 == 1 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 28 then
                self.进度 = 2
            end
        end
    elseif NPC.名称 == '平等王' then
        if self.进度 == 2 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 36 then
                self.进度 = 3
            end
        elseif self.进度 == 3 then
            NPC.台词 = _台词[37].台词
        elseif self.进度 == 4 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 52 then
                self.进度 = 5
            end
        end
    elseif NPC.名称 == '马面' and 玩家.地图 == 1128 then
        if self.进度 == 5 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 57 then
                self.进度 = 6
            end
        elseif self.进度 == 8 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 69 then
                local 添加物品 = 玩家:添加物品({ 生成物品 { 名称 = '移魂换魄简', 数量 = 1, 禁止交易 = true } })
                if 添加物品 then
                    self.进度 = 9
                else
                    玩家:提示窗口('#Y你的背包满了，无法获得任务物品！')
                    self.对话进度 = self.对话进度 - 1
                end
            end
        end
    elseif NPC.名称 == '天一神将' then
        if self.进度 == 6 then
            if self.对话进度 >= 62 then
                self.对话进度 = 57
            end
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 62 then
                self:任务攻击事件(玩家, NPC)
            end
        end
