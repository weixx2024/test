        if self.进度 == 9 then
            if self.对话进度 < 72 then
                if 玩家.外形 == 2064 then
                    self.对话进度 = 72
                else
                    self.对话进度 = 70
                end
                NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            else
                self.对话进度 = self.对话进度 + 1
                if self.对话进度 == 73 then
                    local r = 玩家:取任务('变身卡')
                    if r then
                        r:删除()
                    end
                    玩家:刷新外形()
                end
                NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
                if self.对话进度 == 77 then
                    self.进度 = 10
                    玩家:添加物品({ 生成物品 { 名称 = '附魂珠', 数量 = 1, 禁止交易 = true } })
                end
            end
        end
    end
end

