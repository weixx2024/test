function 任务:任务攻击事件(玩家, NPC)
    if NPC.名称 == '摄魂鬼' then
        if self.进度 == 1 then
            local r = 玩家:进入战斗('scripts/task/称谓剧情/称谓4_摄魂鬼.lua')
            if r then
                self.对话进度 = 1
                NPC.头像 = NPC.外形
                NPC.台词 = _台词[4]
                NPC.结束 = false
            end
        end
    end
end

