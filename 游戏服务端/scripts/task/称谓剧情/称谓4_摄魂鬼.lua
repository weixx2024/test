function 任务:击杀摄魂鬼(玩家)
    self.进度 = 2
    self.对话进度 = 4
    玩家:添加经验(254214)
    玩家:添加师贡(36542)
    玩家:常规提示('#Y你打败了摄魂鬼，获得了254214经验和36542师贡。')
end

function 任务:完成(玩家)
    玩家:添加声望(150)
    玩家:添加物品({ 生成物品 { 名称 = '龙之鳞', 数量 = 1, 禁止交易 = true } })
    玩家:提示窗口('#Y因为你的为民除害，你在这个世界的声望得到获得150点声望和龙之鳞。')

    local r = 玩家:取任务('引导_称谓剧情')
    if r then
        r:检测剧情称谓是否完成(玩家, 4, '摄魂鬼')
    end
    self:删除()
end

