function 任务:取对话(玩家)
    local r = _台词[self.对话进度]
    local 台词, 头像, 结束 = r.台词, r.头像, r.结束
    if 头像 == 0 then
        头像 = 玩家.原形
    end
    return 台词, 头像, 结束
end

function 任务:任务NPC对话(玩家, NPC)
    if not 玩家:剧情称谓是否存在(3) then
        return
    end

    NPC.队伍对话 = true
    if NPC.名称 == '幸存者' then
        if self.进度 == 0 then
            self.对话进度 = self.对话进度 + 1
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            if self.对话进度 == 3 then
                self.进度 = 1
            end
        elseif self.进度 == 2 then
            self.对话进度 = 6
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
            self:完成(玩家)
        end
    elseif NPC.名称 == '摄魂鬼' then
        if self.进度 == 2 and self.对话进度 == 4 then
            self.对话进度 = 5
            NPC.台词, NPC.头像, NPC.结束 = self:取对话(玩家)
        end
    end
end

