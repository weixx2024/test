function 任务:击杀春三十娘(玩家)
    self.进度 = 2
    玩家:添加经验(445458)
    玩家:添加师贡(23987)
    玩家:常规提示('#R你获得445458经验和23987两银子！')
end

function 任务:完成(玩家)
    玩家:添加声望(300)
    玩家:提示窗口('#Y因为你打败了有名的魔头，你在这个世界的声得到了提升，获得300点声望。')
    local r = 玩家:取任务('引导_称谓剧情')
    if r then
        r:检测剧情称谓是否完成(玩家, 3, '春三十娘')
    end
    self:删除()
end

function 任务:任务攻击事件(玩家, NPC)
    if NPC.名称 == '春三十娘' then
        if self.进度 == 1 then
            玩家:进入战斗('scripts/task/称谓剧情/称谓3_春三十娘.lua')
        end
    end
end

