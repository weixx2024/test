function 任务:添加任务(玩家)
    local map = 玩家:取随机地图(_地图)

    if not map then
        return
    end
    local X, Y = map:取随机坐标()
    if not X then
        return
    end

    if 玩家.是否组队 then
        local t = {}
        for _, v in 玩家:遍历队伍() do
            if v:判断等级是否低于(10) then --or not v:剧情称谓是否存在(8)
                table.insert(t, v.名称)
            end
        end
        if #t > 0 then
            玩家:常规提示('#Y' .. table.concat(t, '、 ') .. '#W低于10级,无法领取')
            return
        end
        for _, v in 玩家:遍历队伍() do
            for _, name in pairs(_任务类型) do
                if v:取任务(name) then
                    table.insert(t, v.名称)
                end
            end
        end
        if #t > 0 then
            玩家:常规提示('#Y' .. table.concat(t, '、 ') .. '已有此任务,无法重复领取')
            return
        end



        self.队伍 = {}
        for k, v in 玩家:遍历队伍() do
            table.insert(self.队伍, v.nid)
            v.其它.元宵次数 = v.其它.元宵次数 + 1
            if v.其它.元宵次数 > 6 then
                v.其它.元宵次数 = 1
            end
            v:添加任务(self)
        end
    else
        if 玩家:判断等级是否低于(10) then
            玩家:常规提示('#Y低于10级,无法领取')
            return
        end
        for _, name in pairs(_任务类型) do
            if 玩家:取任务(name) then
                玩家:常规提示('#Y已有此任务,无法重复领取')
                return
            end
        end
        self.队伍 = {}
        table.insert(self.队伍, 玩家.nid)
        玩家.其它.元宵次数 = 玩家.其它.元宵次数 + 1
        if 玩家.其它.元宵次数 > 6 then
            玩家.其它.元宵次数 = 1
        end
        玩家:添加任务(self)
    end
    self.怪名 = _怪名[math.random(#_怪名)]
    self.位置 = string.format('%s(%d,%d)', map.名称, X, Y)
    self.寻路 = string.format('#u#[%s|%s|%s|$%s#]#u', map.id, X, Y, self.怪名)
    self.时间 = os.time() + 30 * 60
    self.NPC =
    map:添加NPC {
        队伍 = self.队伍,
        人数 = #self.队伍,
        名称 = self.怪名,
        时长 = 1800,
        外形 = math.random(46, 47),
        脚本 = 'scripts/task/节日活动/元宵_童心祝福.lua',
        时间 = 1800,
        任务类型 = "童心祝福",
        X = X,
        Y = Y,
        来源 = self
    }






    self.队长 = 玩家.名称
    self.MAP = map.id
    self.次数 = 玩家.其它.元宵次数
    self.x = X
    self.y = Y
    return string.format(_提示, self.怪名, self.位置)
end

function 任务:完成(玩家, 胜利)
    if 胜利 then
        self:掉落包(玩家)
    end
    local map = 玩家:取地图(self.MAP)
    if map then
        map:删除NPC(self.NPC)
    end
    self:删除()
end

